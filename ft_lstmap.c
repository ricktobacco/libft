#include "libft.h"

t_list	*ft_lstmap(t_list *lst, t_list *(*f)(t_list *elem))
{
	t_list *first;
	t_list *tmp;

	if (!lst)
		return (NULL);
	tmp = f(lst);
	first = tmp;
	while (lst->next)
	{
		lst = lst->next;
		if (!(tmp->next = f(lst)))
		{
			free(tmp->next);
			return (0);
		}
		tmp = tmp->next;
	}
	return (first);
}
